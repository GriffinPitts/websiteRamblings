function changeFontRed() {
    var x = document.getElementById("head");

    x.style.color = "red"; 
    //x.style["-webkit-text-stroke"] = "1px black";
}

function changeFontBlue() {
    var x = document.getElementById("head");   

    x.style.color = "blue"; 
    //x.style["-webkit-text-stroke"] = "1px black";
}
function changeFontDefault() {
    var x = document.getElementById("head");   
    
    x.style.color = "white"; 
    //x.style.removeProperty("-webkit-text-stroke");
}
